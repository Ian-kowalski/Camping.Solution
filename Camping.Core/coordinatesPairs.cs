namespace camping.Core
{
    public class CoordinatesPairs
    {
        public int _x1;
        public int _y1;
        public int _x2;
        public int _y2;

        public CoordinatesPairs(int x1, int y1, int x2, int y2)
        {
            _x1 = x1;
            _y1 = y1;
            _x2 = x2;
            _y2 = y2;
        }
    }
}
